package repetitionstructures;

public class WhileLoopDisplayNames {

	public static void main(String[] args) {
		// use the while loop to display some names
		String [] names = {"Trump", "Obama", "Bush", "Clinton"};
		int i = 0;
		while (i < names.length){
		
		System.out.println("Name: " +names[i]);
		
		i++;
	}

	}
}



